        usuarioActual = usuarioDao.findById(id);
        jTextFieldNombreUsuario.setText(usuarioActual.getNombre());
        jTextFieldNuevoPassword.setText(usuarioActual.getClave());
        jTextFieldRePassword.setText(usuarioActual.getClave());
